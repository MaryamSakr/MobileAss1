class Student{
  String? name;
  String? gender ;
  String? email;
  String? studentID;
  String? level;
  String? password ;

  Student(name,gender,email,studentID,level,password){
    this.name = name;
    this.email = email;
    this.gender = gender;
    this.password = password;
    this.studentID = studentID;
    this.level = level;
  }
}